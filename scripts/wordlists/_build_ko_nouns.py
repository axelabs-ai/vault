def main(out_path: Path) -> int:
    seen: set[str] = set()
    ordered: list[str] = []
    per_cat: dict[str, int] = {}

    for name, pool in CATEGORIES.items():
        added = 0
        for raw in pool:
            w = unicodedata.normalize("NFC", raw).strip()
            if not is_clean_korean(w):
                continue
            if w in seen:
                continue
            seen.add(w)
            ordered.append(w)
            added += 1
        per_cat[name] = added

    # Sort for deterministic output
    ordered_sorted = sorted(set(ordered))
    # Trim to exactly 4096 (2^12) for clean 12 bits/word entropy.
    # Falls back to 2048 (2^11, 11 bits/word) if pool is smaller.
    if len(ordered_sorted) >= 4096:
        target = 4096
    elif len(ordered_sorted) >= 2048:
        target = 2048
    else:
        target = len(ordered_sorted)
    final = ordered_sorted[:target]
    out_path.write_text("\n".join(final) + "\n", encoding="utf-8")

    print(f"Pool size: {len(ordered_sorted)} unique nouns", file=sys.stderr)
    print(f"Wrote {len(final)} nouns to {out_path} (target {target})", file=sys.stderr)
    for k, v in per_cat.items():
        print(f"  {k:10s} {v}", file=sys.stderr)
    return len(final)


if __name__ == "__main__":
    out = Path(sys.argv[1]) if len(sys.argv) > 1 else Path(__file__).with_name("ko_nouns.txt")
    n = main(out)
    if n < 2048:
        print(f"[!] Only {n} unique nouns - below minimum 2048", file=sys.stderr)
        sys.exit(1)
