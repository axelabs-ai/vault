# Hangul syllable block U+AC00..U+D7A3
HANGUL_RE = re.compile(r"^[가-힣]{1,3}$")


def is_clean_korean(w: str) -> bool:
    w = w.strip()
    if not w:
        return False
    if not HANGUL_RE.match(w):
        return False
    return 1 <= len(w) <= 3


